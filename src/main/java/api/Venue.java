package api;

public class Venue {

	private String city;
	private String country;
	private String street;
	private String postalCode;
	private GeoLocation geoLocation;
	private String venueName;
	private String venueId;
	private String venueWebsite;

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getStreet() {
		return street;
	}

	public void setStreet(String street) {
		this.street = street;
	}

	public String getPostalCode() {
		return postalCode;
	}

	public void setPostalCode(String postalCode) {
		this.postalCode = postalCode;
	}

	public GeoLocation getGeoLocation() {
		return geoLocation;
	}

	public void setGeoLocation(GeoLocation geoLocation) {
		this.geoLocation = geoLocation;
	}

	public String getVenueName() {
		return venueName;
	}

	public void setVenueName(String venueName) {
		this.venueName = venueName;
	}

	public String getVenueId() {
		return venueId;
	}

	public void setVenueId(String venueId) {
		this.venueId = venueId;
	}

	public String getVenueWebsite() {
		return venueWebsite;
	}

	public void setVenueWebsite(String venueWebsite) {
		this.venueWebsite = venueWebsite;
	}

	public String toString() {
		String response = "";
		response = "Venue-ID: " + venueId + "\n" + "Venue-Name: " + venueName
				+ "\n" + "Location:\n" + geoLocation.toString() + "City: "
				+ city + "\n" + "Country: " + country + "\n" + "Street: "
				+ street + "\n" + "Postalcode: " + postalCode + "\n" + "Url: "
				+ venueWebsite;
		return response;
	}

}
